package com.cookandroid.cbt7;

public class articleList {
    private String articleImage;
    private String articleTitle;
    private String articlekeyword;
    private String articlewriter;
    private String articletime;
    private int articleviews;

    public articleList(){}

    public String getArticleImage() {
        return articleImage;
    }
    public void setArticleImage(String articleImage) {
        this.articleImage = articleImage;
    }

    public String getArticleTitle() {
        return articleTitle;
    }
    public void setArticleTitle() {
        this.articleTitle = articleTitle;
    }

    public String getArticlekeyword() {
        return articlekeyword;
    }
    public void setArticlekeyword() {
        this.articlekeyword = articlekeyword;
    }

    public String getArticlewriter() {
        return articlewriter;
    }
    public void setArticlewriter() {
        this.articlewriter = articlewriter;
    }

    public String getArticletime() {
        return articletime;
    }
    public void setArticletime() {
        this.articletime = articletime;
    }

    public int getArticleviews() {
        return articleviews;
    }
    public void setArticleviews() {
        this.articleviews = articleviews;
    }
}
